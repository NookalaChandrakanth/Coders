package corejava_programmes;

public class Math_programme {
	
	public static void main(String[] args) {
		
		System.out.println(Math.addExact(506, 494));
		System.out.println(Math.addExact(566554451, 545445454));
		System.out.println(Math.subtractExact(1000, 950));
		System.out.println(Math.subtractExact(5555555, 450056));
		System.out.println(Math.multiplyExact(45, 15));
		System.out.println(Math.multiplyExact(55554, 5));
		System.out.println(Math.multiplyExact(50000, 2000));
		System.out.println(Math.floorDiv(50, 5));
		System.out.println(Math.floorDiv(50000000, 50));
		System.out.println(Math.floorDiv(500000, 5000));
		System.out.println(Math.max(2000, 2001));
		System.out.println(Math.min(10,8));
		
		System.out.println(Math.floorMod(50, 5));
		System.out.println(Math.PI);
		System.out.println(Math.random());
		System.out.println(Math.sqrt(64));
		System.out.println(Math.abs(-121));
		
	}
	

}
